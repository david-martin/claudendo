// ---------------------------------------------------------------------------
// Internal helper: plot a single logical pixel with bounds-checking, using the
// 3DS rotated framebuffer mapping  index = x*H + (H-1-y).
// ---------------------------------------------------------------------------
static inline void put_px(const GfxTarget *t, int x, int y, u16 color) {
    if (x < 0 || x >= t->w || y < 0 || y >= t->h) return;
    t->fb[x * t->h + (t->h - 1 - y)] = color;
}

void gfx_pixel(const GfxTarget *t, int x, int y, u16 color) {
    put_px(t, x, y, color);
}

void gfx_clear(const GfxTarget *t, u16 color) {
    for (int x = 0; x < t->w; x++) {
        for (int y = 0; y < t->h; y++) {
            t->fb[x * t->h + (t->h - 1 - y)] = color;
        }
    }
}

void gfx_fill_rect(const GfxTarget *t, int x, int y, int w, int h, u16 color) {
    for (int j = 0; j < h; j++) {
        for (int i = 0; i < w; i++) {
            put_px(t, x + i, y + j, color);
        }
    }
}

void gfx_border(const GfxTarget *t, int x, int y, int w, int h, int thick, u16 color) {
    if (thick <= 0 || w <= 0 || h <= 0) return;
    if (thick > h) thick = h;
    if (thick > w) thick = w;
    // Top and bottom bars (full width).
    gfx_fill_rect(t, x, y, w, thick, color);
    gfx_fill_rect(t, x, y + h - thick, w, thick, color);
    // Left and right bars (between the top/bottom bars to avoid redundancy).
    int inner_h = h - 2 * thick;
    if (inner_h > 0) {
        gfx_fill_rect(t, x, y + thick, thick, inner_h, color);
        gfx_fill_rect(t, x + w - thick, y + thick, thick, inner_h, color);
    }
}

void gfx_darken_rect(const GfxTarget *t, int x, int y, int w, int h) {
    for (int j = 0; j < h; j++) {
        int py = y + j;
        if (py < 0 || py >= t->h) continue;
        for (int i = 0; i < w; i++) {
            int px = x + i;
            if (px < 0 || px >= t->w) continue;
            int idx = px * t->h + (t->h - 1 - py);
            u16 c = t->fb[idx];
            t->fb[idx] = (u16)((c >> 1) & 0x7BEF); // halve each RGB565 channel
        }
    }
}

// ---------------------------------------------------------------------------
// Draw a single glyph cell at logical (px,py), scaled by `scale`.
// If `opaque` is non-zero, the full cell is first filled with `bg`.
// ---------------------------------------------------------------------------
static void draw_glyph(const GfxTarget *t, int px, int py, int scale,
                       u16 fg, u16 bg, int opaque, unsigned char ch) {
    if (scale < 1) scale = 1;
    if (opaque) {
        gfx_fill_rect(t, px, py, 8 * scale, 8 * scale, bg);
    }
    if (ch >= 128) return; // outside table: render as blank
    const unsigned char *glyph = font8x8_basic[ch];
    for (int row = 0; row < 8; row++) {
        unsigned char bits = glyph[row];
        if (!bits) continue;
        for (int col = 0; col < 8; col++) {
            if (bits & (1 << col)) { // bit 0 (LSB) = leftmost pixel
                // Scaled block for this set bit.
                int bx = px + col * scale;
                int by = py + row * scale;
                for (int sy = 0; sy < scale; sy++) {
                    for (int sx = 0; sx < scale; sx++) {
                        put_px(t, bx + sx, by + sy, fg);
                    }
                }
            }
        }
    }
}

void gfx_text(const GfxTarget *t, int x, int y, int scale, u16 fg, const char *s) {
    if (scale < 1) scale = 1;
    int cx = x;
    for (; *s; s++) {
        draw_glyph(t, cx, y, scale, fg, 0, 0, (unsigned char)*s);
        cx += 8 * scale;
    }
}

void gfx_text_bg(const GfxTarget *t, int x, int y, int scale, u16 fg, u16 bg, const char *s) {
    if (scale < 1) scale = 1;
    int cx = x;
    for (; *s; s++) {
        draw_glyph(t, cx, y, scale, fg, bg, 1, (unsigned char)*s);
        cx += 8 * scale;
    }
}

int gfx_text_width(int scale, const char *s) {
    if (scale < 1) scale = 1;
    return (int)strlen(s) * 8 * scale;
}

int gfx_text_height(int scale) {
    if (scale < 1) scale = 1;
    return 8 * scale;
}

// ---------------------------------------------------------------------------
// Greedy word-wrap. Words are separated by spaces. A word longer than the box
// is hard-broken across lines. '\n' forces a line break.
// ---------------------------------------------------------------------------
int gfx_text_wrap(const GfxTarget *t, int x, int y, int boxw, int scale,
                  int line_gap, u16 fg, const char *s) {
    if (scale < 1) scale = 1;
    const int cw = 8 * scale;                 // per-character width
    const int adv = gfx_text_height(scale) + line_gap;
    // Max characters that fit on one line. At least 1 to guarantee progress.
    int max_chars = (cw > 0) ? (boxw / cw) : 0;
    if (max_chars < 1) max_chars = 1;

    int lines = 0;
    int cur_y = y;
    int line_len = 0;          // chars currently buffered on this line
    char line[256];

    const char *p = s;
    while (*p) {
        // Handle forced newline.
        if (*p == '\n') {
            line[line_len] = '\0';
            gfx_text(t, x, cur_y, scale, fg, line);
            lines++;
            cur_y += adv;
            line_len = 0;
            p++;
            continue;
        }

        // Measure the next word (run of non-space, non-newline chars).
        const char *ws = p;
        int word_len = 0;
        while (ws[word_len] && ws[word_len] != ' ' && ws[word_len] != '\n')
            word_len++;

        if (word_len == 0) {
            // Current char is a space.
            if (line_len > 0) {
                if (line_len + 1 <= max_chars && line_len + 1 < (int)sizeof(line) - 1) {
                    line[line_len++] = ' ';
                } else {
                    // Space would overflow: break the line here, drop the space.
                    line[line_len] = '\0';
                    gfx_text(t, x, cur_y, scale, fg, line);
                    lines++;
                    cur_y += adv;
                    line_len = 0;
                }
            }
            // Leading spaces at start of a line are skipped.
            p++;
            continue;
        }

        // Word longer than the box: hard-break it.
        if (word_len > max_chars) {
            // Flush whatever is on the current line first.
            if (line_len > 0) {
                line[line_len] = '\0';
                gfx_text(t, x, cur_y, scale, fg, line);
                lines++;
                cur_y += adv;
                line_len = 0;
            }
            while (word_len > 0) {
                int chunk = (word_len > max_chars) ? max_chars : word_len;
                for (int i = 0; i < chunk; i++) line[i] = p[i];
                line[chunk] = '\0';
                gfx_text(t, x, cur_y, scale, fg, line);
                lines++;
                cur_y += adv;
                p += chunk;
                word_len -= chunk;
            }
            line_len = 0;
            continue;
        }

        // Word fits on a line by itself; does it fit on the current line?
        int need = (line_len > 0) ? (line_len + 1 + word_len) : word_len;
        if (need > max_chars) {
            // Wrap: flush current line, start fresh with this word.
            line[line_len] = '\0';
            gfx_text(t, x, cur_y, scale, fg, line);
            lines++;
            cur_y += adv;
            line_len = 0;
        }
        if (line_len > 0) line[line_len++] = ' ';
        for (int i = 0; i < word_len; i++) line[line_len++] = p[i];
        p += word_len;
    }

    // Flush any trailing content.
    if (line_len > 0) {
        line[line_len] = '\0';
        gfx_text(t, x, cur_y, scale, fg, line);
        lines++;
    }

    return lines;
}
